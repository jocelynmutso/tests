package com.blobtest1;

public class AdditionMultiplication {

	public static void main(String[] args) {

		int a = 2;
		int b = 4;
		int additionSum = a + b;
		String sum = "2 + 4 = ";

		int c = 4;
		int d = 5;
		int product = c * d;
		String productFormula = "4 * 5 = ";

		System.out.println(sum + additionSum);
		System.out.println(productFormula + product);

	}
}
